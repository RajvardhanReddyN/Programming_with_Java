class Sum4
{
    public static void ser(int n)
    
    {
        double Sum=0;
        int S=0;
        
        for(int i=1;i<=n;i++)
        {
            S=S+i;
            Sum=Sum+S;
        }
        
        
        System.out.println("Sum = " + Sum);
        
    }
    
    
    
    
    
    
    
    
    
}